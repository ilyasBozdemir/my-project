
package tekrarlar;

public class For {
    public static void main(String[] args) {
         for (int sayi =0 ; sayi <5; sayi++) 
         {
             System.out.println("sayi =" + sayi);
         }
         for (int x =0 , y =0;x<6;x++,y++)
         {
             x*=y;
           System.out.println("x ="+ x + "y =" +y); 
         }
    }
}
